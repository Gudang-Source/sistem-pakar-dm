<div class="title">Informasi Sistem Pakar </div>
<br />
<b>Sistem pakar</b> (<i>expert system</i>) adalah sistem yang berusaha mengapdosi pengetahuan manusia ke komputer, agar komputer dapat menyelesaikan masalah seperti yang biasa dilakukan oleh para ahli. Sistem pakar yang baik dirancang agar dapat menyelesaikan suatu permasalahan tertentu dengan meniru kerja dari para ahli.
<br>
<br />
<b>Sistem Pakar Diagnosa Penyakit DIABETES MELITUS </b> adalah sarana untuk  konsultasi penyakit Diabetes Melitus, karena penyakit Diabetes Melitus memiliki 2 tipe yaitu Tipe 1 dan Tipe 2.<br />
Sumber basis pengetahuan dari sistem ini diperoleh dari hasil studi pustaka dari berbagai sumber yang berkaitan dengan Basis Pengetahuan Penyakit Diabetes Melitus.<br />
